// power....
let num1 = Math.pow(2,3);
console.log(num1);

// absolute vale(that means -5 == 5);
let num2 = Math.abs(20-40);
console.log(num2);

// round number:
let num3  = Math.round(2.3);
console.log(num3);

// ceiling 
let num4 = Math.ceil(2.22435);
console.log(num4);

// floor:
let num5 = Math.floor(2.87589);
console.log(num5);


